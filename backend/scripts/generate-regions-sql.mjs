import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { TextDecoder } from "node:util";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const defaultInputPath = path.resolve(
  __dirname,
  "../src/main/resources/법정동코드 전체자료.txt",
);
const defaultOutputPath = path.resolve(
  __dirname,
  "../src/main/resources/generated/regions-insert.sql",
);

const inputPath = path.resolve(process.argv[2] ?? defaultInputPath);
const outputPath = path.resolve(process.argv[3] ?? defaultOutputPath);

function readRows(filePath) {
  const content = decodeFile(filePath).replace(/^\uFEFF/, "");
  const lines = content.split(/\r?\n/).filter(Boolean);

  if (lines.length < 2) {
    throw new Error("입력 파일에 데이터가 없습니다.");
  }

  return lines.slice(1).map((line, index) => {
    const [code, fullName, status] = line.split("\t");

    if (!code || !fullName || !status) {
      throw new Error(`잘못된 데이터 형식입니다. line=${index + 2}`);
    }

    return {
      code: code.trim(),
      fullName: fullName.trim(),
      status: status.trim(),
    };
  });
}

function decodeFile(filePath) {
  const buffer = fs.readFileSync(filePath);
  const utf8 = buffer.toString("utf8");

  if (utf8.includes("법정동코드") && utf8.includes("폐지여부")) {
    return utf8;
  }

  return new TextDecoder("euc-kr").decode(buffer);
}

function isEmdLevel(code) {
  return code.length === 10 && code.slice(5, 8) !== "000" && code.slice(8, 10) === "00";
}

function toSqlString(value) {
  return `'${value.replaceAll("\\", "\\\\").replaceAll("'", "''")}'`;
}

function chunk(array, size) {
  const chunks = [];

  for (let index = 0; index < array.length; index += size) {
    chunks.push(array.slice(index, index + size));
  }

  return chunks;
}

function buildRegions(rows) {
  const nameByCode = new Map(rows.map((row) => [row.code, row.fullName]));

  return rows
    .filter((row) => row.status === "존재" && isEmdLevel(row.code))
    .map((row) => {
      const sidoCode = `${row.code.slice(0, 2)}00000000`;
      const sigunguCode = `${row.code.slice(0, 5)}00000`;
      const sigunguFullName = nameByCode.get(sigunguCode);
      const sido = nameByCode.get(sidoCode) ?? sigunguFullName;

      if (!sigunguFullName || !sido) {
        throw new Error(`상위 행정구역을 찾을 수 없습니다. code=${row.code}`);
      }

      const sigungu = sigunguFullName.startsWith(`${sido} `)
        ? sigunguFullName.slice(sido.length + 1)
        : sigunguFullName;

      const dong = row.fullName.startsWith(`${sigunguFullName} `)
        ? row.fullName.slice(sigunguFullName.length + 1)
        : row.fullName.split(" ").at(-1);

      return {
        code: row.code,
        sido,
        sigungu,
        dong,
      };
    });
}

function buildSql(regions) {
  const header = [
    "-- Generated by scripts/generate-regions-sql.mjs",
    `-- Source: 법정동코드 전체자료.txt`,
    `-- Rows: ${regions.length}`,
    "",
  ];

  const statements = chunk(regions, 500).map((group) => {
    const values = group
      .map(
        (region) =>
          `  (${toSqlString(region.sido)}, ${toSqlString(region.sigungu)}, ${toSqlString(region.dong)})`,
      )
      .join(",\n");

    return `INSERT INTO regions (sido, sigungu, dong)\nVALUES\n${values};`;
  });

  return `${header.join("\n")}${statements.join("\n\n")}\n`;
}

function main() {
  const rows = readRows(inputPath);
  const regions = buildRegions(rows);
  const sql = buildSql(regions);

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, sql, "utf8");

  console.log(`input: ${inputPath}`);
  console.log(`output: ${outputPath}`);
  console.log(`regions: ${regions.length}`);
}

main();
